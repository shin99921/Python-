# 튜플의 슬라이싱과 ,+ ,*
# 인덱싱
print("2","-"*50)
t1 = (1,2,'a','b')
print(t1[0])
print(t1[3])
print("\n")

# 슬라이딩
print("3","-"*50)
t = (1,2,3,'a','b')
print(t[1:])
print(t[2:])
print(t[-1:])
print(t[:-1])
print(t[:2])
print(t[1:3])
print("\n")

# 튜플 더하기
print("4","-"*50)
t1 = (1,2,'a','b')
t2 = (3,4)
print(t1+t2)
print("\n")

# 튜플 곱하기
print("5","-"*50)
print(t2*10)