# 튜플 자료형
# 리스트는 []로 둘러싸여있고 튜플은 ()로 싸여있다.
# 리스트는 삭제,수정이 가능하지만 튜플은 값을 바꿀수 없다.

t1 = ()
t2 = (1,) # 요소값이 하나일때 무조건 뒤에 콤마를 붙여야 함.
t3 = (1,2,3)
t4 = 1,2,3 # 튜플은 괄호를 생략해도 무관하다.
t5 = ('a','b',('ab','cd'))
print("1","-"*50)
print(t1)
print(t2)
print(t3)
print(t4)
print(t5)
print("\n")

